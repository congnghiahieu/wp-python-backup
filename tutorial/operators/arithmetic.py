# Cộng, trừ, nhân
print(2 + 3)
print(2 - 3)
print(2 * 3)

# Chia lấy phần thập phân
print(33 /  5)

# Chia lấy phần nguyên
print(33 // 5)

# Chia lấy phần dư
print(33 % 5)

# Sử dụng hàm round để làm tròn kết quả
print(round(32 / 5)) # 6.4 => 6
print(round(33 / 5)) # 6.6 => 7

# Luỹ thừa
print(2 ** 5)
